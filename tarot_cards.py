def draw_cards(n: int) -> list:
    """Вытащить n случайных карт без повторений"""
    return random.sample(TAROT_CARDS, n)


def format_cards_text(cards: list) -> str:
    """Форматировать список карт для отображения"""
    result = []
    for i, card in enumerate(cards, 1):
        result.append(
            f"🃏 *Карта {i}: {card['name']}*\n"
            f"   Стихия: {card['element']} | Ключевые слова: _{card['keywords']}_"
        )
    return "\n\n".join(result)
